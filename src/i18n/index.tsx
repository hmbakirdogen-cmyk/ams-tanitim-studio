const DICT: Record<Lang, Record<string, string>> = { tr: {}, en: EN, ja: JA }

function load(): Lang {
  try {
    const v = localStorage.getItem(KEY) as Lang
    if (LANGS.includes(v)) return v // kullanıcı SEÇİMİ öncelikli + kalıcı (LangSwitcher localStorage'a yazar)
    // Kayıt YOKSA tarayıcı diline göre OTOMATİK: Japonya/uluslararası izleyici ilk açılışta TÜRKÇE görmesin.
    //   ja*→ja (ev sahibi), tr*→tr, diğer→en (uluslararası güvenli varsayılan). Kullanıcı yine 3 sekmeden değiştirir.
    const n = (navigator.language || (navigator.languages && navigator.languages[0]) || '').toLowerCase()
    if (n.startsWith('ja')) return 'ja'
    if (n.startsWith('tr')) return 'tr'
    return 'en'
  } catch {
    return 'tr'
  }
}

let current: Lang = load()
const listeners = new Set<() => void>()
if (typeof document !== 'undefined') document.documentElement.lang = current

export function getLang(): Lang {
  return current
}
export function setLang(l: Lang): void {
  if (l === current || !LANGS.includes(l)) return
  current = l
  try { localStorage.setItem(KEY, l) } catch { /* offline */ }
  if (typeof document !== 'undefined') document.documentElement.lang = l
  listeners.forEach((f) => f())
}

// Cekirdek ceviri: tr -> oldugu gibi; en/ja -> sozluk, yoksa Turkce'ye duser
export function translate(s: string, l: Lang = current): string {
  return l === 'tr' ? s : (DICT[l][s] ?? s)
}

// React disi (veri katmani) - reaktif DEGIL
export function t(s: string): string {
  return translate(s)
}

// Bilesenlerde - dil degisince otomatik yeniden render
export function useLang() {
  const lang = useSyncExternalStore(
    (cb) => { listeners.add(cb); return () => listeners.delete(cb) },
    () => current,
    () => current,
  )
  return { lang, setLang, t: (s: string) => translate(s, lang) }
}
